let num = 10;
let square = num * num;
if (square > 100) {
  console.log(square + " მეტია 100-ზე");
} else if (num == 100) {
  console.log(square + " ნაკლებია 100ზე");
} else {
  console.log("კვადრატი 100ია");
}

